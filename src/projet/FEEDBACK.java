package projet;

public class FEEDBACK {
	String Nom;
	String FEED;
	
	
	
	public FEEDBACK(String nom, String fEED) {
		Nom = nom;
		FEED = fEED;
	}
	
	public String getNom() {
		return Nom;
	}
	public void setNom(String nom) {
		Nom = nom;
	}
	public String getFEED() {
		return FEED;
	}
	public void setFEED(String fEED) {
		FEED = fEED;
	}
	
	
}
